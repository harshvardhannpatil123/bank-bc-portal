let currentLanguage = localStorage.getItem("language") || "en";
const langBtn = document.getElementById("langBtn");

function updateLanguage() {
    const t = translations[currentLanguage];

    document.getElementById("heroTitle").textContent = t.heroTitle;
    document.getElementById("heroSubtitle").textContent = t.heroSubtitle;
    document.getElementById("welcomeTitle").textContent = t.welcomeTitle;
    document.getElementById("welcomeText").textContent = t.welcomeText;
    document.getElementById("statusTitle").textContent = t.statusTitle;
document.getElementById("currentStatus").textContent = t.statusAvailable;
document.getElementById("statusMessage").textContent = t.statusMessage;
document.getElementById("servicesTitle").textContent = t.servicesTitle;

document.getElementById("accountOpening").textContent = t.accountOpening;
document.getElementById("accountOpeningDesc").textContent = t.accountOpeningDesc;

document.getElementById("cashDeposit").textContent = t.cashDeposit;
document.getElementById("cashDepositDesc").textContent = t.cashDepositDesc;

document.getElementById("cashWithdrawal").textContent = t.cashWithdrawal;
document.getElementById("cashWithdrawalDesc").textContent = t.cashWithdrawalDesc;

document.getElementById("moneyTransfer").textContent = t.moneyTransfer;
document.getElementById("moneyTransferDesc").textContent = t.moneyTransferDesc;

document.getElementById("rdOpening").textContent = t.rdOpening;
document.getElementById("rdOpeningDesc").textContent = t.rdOpeningDesc;

document.getElementById("fdOpening").textContent = t.fdOpening;
document.getElementById("fdOpeningDesc").textContent = t.fdOpeningDesc;

    localStorage.setItem("language", currentLanguage);

    langBtn.innerHTML = currentLanguage === "en" ? "ಕನ್ನಡ" : "English";
}

langBtn.addEventListener("click", () => {
    currentLanguage = currentLanguage === "en" ? "kn" : "en";
    updateLanguage();
});

updateLanguage();